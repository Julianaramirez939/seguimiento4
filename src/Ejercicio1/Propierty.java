package Ejercicio1;

public class Propierty {
        private String address;
        private int cost;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    public Propierty(String address, int cost) {
        this.address = address;
        this.cost = cost;

    }
}




